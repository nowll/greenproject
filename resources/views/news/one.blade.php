                <h2 class="text-2xl font-bold text-gray-800 mt-8 mb-4">What is SDG 12?</h2>
                <p class="mb-4">
                    SDG 12 aims to ensure sustainable consumption and production patterns. It focuses on reducing waste,
                    improving resource efficiency, and promoting sustainable practices across all sectors, from
                    individuals to industries.
                </p>
                <ul class="list-disc list-inside mb-4 space-y-2">
                    <li><strong>Reducing food waste:</strong> Halve global food waste at the retail and consumer levels.</li>
                    <li><strong>Efficient resource use:</strong> Encourage industries to adopt sustainable practices
                        and technologies.</li>
                    <li><strong>Minimizing environmental impact:</strong> Reduce the release of harmful chemicals and
                        waste into air, water, and soil.</li>
                </ul>
                <p>
                    These practices are essential for reducing the strain on the planet's resources and tackling the root
                    causes of climate change.
                </p>

                <h2 class="text-2xl font-bold text-gray-800 mt-8 mb-4">How Does SDG 12 Connect to Climate Action?</h2>
                <p class="mb-4">
                    The connection between SDG 12 and climate action is profound. Unsustainable consumption and
                    production are among the leading causes of greenhouse gas emissions, resource depletion, and
                    biodiversity loss. Here’s how SDG 12 directly impacts climate action:
                </p>
                <ol class="list-decimal list-inside mb-4 space-y-2">
                    <li><strong>Reducing Emissions Through Sustainable Practices:</strong> Industrial production is a
                        major contributor to global CO₂ emissions. By encouraging resource efficiency and cleaner
                        technologies, SDG 12 directly reduces the carbon footprint of industries.</li>
                    <li><strong>Tackling Waste and Pollution:</strong> Food waste alone contributes to around 8–10% of
                        global greenhouse gas emissions. Implementing strategies to reduce waste significantly lessens
                        methane emissions from landfills.</li>
                    <li><strong>Promoting Renewable Energy and Circular Economies:</strong> Shifting to sustainable
                        production often involves adopting renewable energy sources and recycling materials, reducing
                        dependency on fossil fuels.</li>
                    <li><strong>Empowering Individuals:</strong> By educating consumers on responsible consumption, SDG
                        12 inspires lifestyle changes that collectively reduce environmental impact.</li>
                </ol>

                <h2 class="text-2xl font-bold text-gray-800 mt-8 mb-4">How Can You Support SDG 12?</h2>
                <p class="mb-4">
                    Here are a few actionable steps you can take to support SDG 12 and contribute to climate action:
                </p>
                <ul class="list-disc list-inside mb-4 space-y-2">
