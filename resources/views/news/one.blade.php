                    <li><strong>Support Sustainable Brands:</strong> Choose products from companies that prioritize
                        sustainability.</li>
                    <li><strong>Educate Yourself and Others:</strong> Awareness is key. Share knowledge about SDG 12 and
                        climate action with your community.</li>
                    <li><strong>Advocate for Policy Changes:</strong> Push for government policies that support
                        responsible consumption and production.</li>
                </ul>

